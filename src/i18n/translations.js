export const translate = (key, language = FALLBACK_LANGUAGE, params = {}) => {
  const normalizedLanguage = normalizeLanguage(language);
  const normalizedKey = normalizeLegacyKey(key);
  const value =
    translations[normalizedLanguage][normalizedKey] ??
    translateKnownBranch(normalizedKey, normalizedLanguage) ??
    translations[FALLBACK_LANGUAGE][normalizedKey] ??
    normalizedKey;

  return Object.entries(params).reduce(
    (text, [param, replacement]) =>
      text.replaceAll(`{{${param}}}`, String(replacement)),
    value,
  );
};

